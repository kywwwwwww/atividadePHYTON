# Código para informar a média final de um aluno que tem 4 bimestres


numero1 = float(input("Digite a nota do primeiro bimestre: "))
numero2 = float(input("Digite a nota do segundo bimestre: "))
numero3 = float(input("Digite a nota do terceiro bimestre: "))
numero4 = float(input("Digite a nota do quarto bimestre: "))

resultado = (numero1 + numero2 + numero3 + numero4) / 4

print(resultado)